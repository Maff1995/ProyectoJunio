    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed

        // TODO add your handling code here:
        if (jButtonAceptar.getText() == "Insertar") {
            try {
                jugador.InsertarJugador(jTextFieldNombre.getText(), jTextFieldApellidos.getText(), jTextFieldPosicion.getText(), Double.parseDouble(jTextFieldSueldo.getText()), Double.parseDouble(jTextFieldAltura.getText()), equipos.getSelectedItem().toString());
                 equipos.getModel().setSelectedItem("Elija un equipo");
                    jTextFieldNombre.setText("");
                    jTextFieldApellidos.setText("");
                    jTextFieldAltura.setText("");
                    jTextFieldPosicion.setText("");
                    jTextFieldSueldo.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(InserAct.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            try {
                jugador.actualizarJugador(id, jTextFieldNombre.getText().toString(), jTextFieldApellidos.getText().toString(), Double.parseDouble(jTextFieldAltura.getText().toString()), jTextFieldPosicion.getText().toString(), Double.parseDouble(jTextFieldSueldo.getText().toString()), equipos.getSelectedItem().toString());
            } catch (SQLException ex) {
                Logger.getLogger(InserAct.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButtonAceptarActionPerformed

