    public InserAct(String id, String nEquipo, String nombre, String apellidos, String altura, String posicion, String sueldo) throws SQLException {

        initComponents();
        if (id == "" || nEquipo == "" || nombre == "" || apellidos == "" || altura == "" || posicion == "" || sueldo == "") {
            jLabelTitulo.setText("Insertar");
            jButtonAceptar.setText("Insertar");
        } else {
            this.id = id;
            jButtonAceptar.setText("Actualizar");
            System.out.println(nombre);
            equipos.getModel().setSelectedItem(nEquipo);
            jTextFieldNombre.setText(nombre);
            jTextFieldApellidos.setText(apellidos);
            jTextFieldAltura.setText(altura);
            jTextFieldPosicion.setText(posicion);
            jTextFieldSueldo.setText(sueldo);

            jLabelTitulo.setText("Actualizar");
        }
        ArrayList<String> lista = new ArrayList<String>();
        lista = ControladorJugador.llenarComboEquipo();
        for (int i = 0; i < lista.size(); i++) {
            equipos.addItem(lista.get(i));
        }
        this.setLocationRelativeTo(null);
    }

