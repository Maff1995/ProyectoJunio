    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:

        if ( jButtonAceptar.getText()=="Insertar"){
       
            try {
                ControladorLiga.InsertarLiga("0",jTextFieldNombre.getText().toString(),jTextFieldPais.getText().toString());
                jTextFieldNombre.setText("");
                jTextFieldPais.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(InserActL.class.getName()).log(Level.SEVERE, null, ex);
            }
                    
        }
        else{
       
            try {
                ControladorLiga.actualizarLiga(this.id,jTextFieldNombre.getText().toString(),jTextFieldPais.getText().toString());
                jTextFieldNombre.setText("");
                jTextFieldPais.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(InserActL.class.getName()).log(Level.SEVERE, null, ex);
            }
          
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

