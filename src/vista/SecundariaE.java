    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
        int row=jTableEquipos.getSelectedRow();
        
        if (row<0){
            JOptionPane.showMessageDialog(null,"Seleccione un Equipo","Error",JOptionPane.PLAIN_MESSAGE);
        }
        else{
        
        String id = jTableEquipos.getValueAt(row, 0).toString();
        controlador.ControladorEquipo.borrarEquipo(Integer.parseInt(id));
        actualizarTabla();  
            
        }
          
    }//GEN-LAST:event_eliminarActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
        int row=jTableEquipos.getSelectedRow();
        
        if (row<0){
            JOptionPane.showMessageDialog(null,"Seleccione un Equipo","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        
            try {
                String id = jTableEquipos.getValueAt(row, 0).toString();
                String nombre = jTableEquipos.getValueAt(row, 1).toString();
                String presupuesto = jTableEquipos.getValueAt(row, 2).toString();
                String nLiga = jTableEquipos.getValueAt(row, 3).toString();
                
                
                InserActE abrir = new InserActE(id,nombre,presupuesto,nLiga);
                abrir.setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(SecundariaE.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
       
        
    }//GEN-LAST:event_actualizarActionPerformed

