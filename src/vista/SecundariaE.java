    public void rellenarTabla() throws SQLException{
        
   DefaultTableModel tablaEquipo = (DefaultTableModel) jTableEquipos.getModel();
     
        ArrayList<Equipo> datos=new ArrayList<>();
        datos=controlador.ControladorEquipo.mostrarEquipos();
      
        for (int i = 0; i < datos.size(); i++) {
            
            int id = datos.get(i).getId();
            String nombre= datos.get(i).getNombre();
            double presupuesto=datos.get(i).getPresupuesto();
            System.out.println(datos.get(i).getIdLiga());
            String nLiga= controlador.ControladorEquipo.nombreLiga(datos.get(i).getMiLiga().getId()) ;
            Liga miLiga=datos.get(i).getMiLiga();
           
            
            tablaEquipo.addRow(new Object[]{id,nombre,presupuesto,nLiga,miLiga.getId()});

        }
    
    }
