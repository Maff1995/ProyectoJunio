    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        // TODO add your handling code here:
       int row=jTableLiga.getSelectedRow();
        
        if (row<0){
            JOptionPane.showMessageDialog(null,"Seleccione un Liga","Error",JOptionPane.PLAIN_MESSAGE);
        }
        else{
        
        String id = jTableLiga.getValueAt(row, 0).toString();
        controlador.ControladorLiga.borrarLiga(id);
        actualizarTabla();  
            
        }
    }//GEN-LAST:event_eliminarActionPerformed

    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
        int row=jTableLiga.getSelectedRow();
        
        if (row<0){
            JOptionPane.showMessageDialog(null,"Seleccione una Liga","Error",JOptionPane.PLAIN_MESSAGE);
        }
        else{
        
            try {
                String nombre = jTableLiga.getValueAt(row, 1).toString();
                String pais = jTableLiga.getValueAt(row, 2).toString();
                String id= jTableLiga.getValueAt(row, 0).toString();;
                InserActL abrir = new InserActL(id,nombre,pais);
                abrir.setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(SecundariaL.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_actualizarActionPerformed

