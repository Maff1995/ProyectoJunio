    public void rellenarTabla() throws SQLException{
        
        DefaultTableModel tablaLiga = (DefaultTableModel) jTableLiga.getModel();
     
        ArrayList<Liga> datos=new ArrayList<>();
        datos=controlador.ControladorLiga.mostrarLigas();
      
        for (int i = 0; i < datos.size(); i++) {
            
            int id = datos.get(i).getId();
            String nombre= datos.get(i).getNombre();
            String pais=datos.get(i).getPais();
            
            
            tablaLiga.addRow(new Object[]{id,nombre,pais});

        }
    
    }
