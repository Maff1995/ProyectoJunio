    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
        int row=jTableJugadores.getSelectedRow();
        
        if (row<0){
            JOptionPane.showMessageDialog(null,"Seleccione un jugador","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        
            try {
                String id = jTableJugadores.getValueAt(row, 0).toString();
                String nEquipo = jTableJugadores.getValueAt(row, 1).toString();
                String nombre = jTableJugadores.getValueAt(row, 2).toString();
                String apellidos = jTableJugadores.getValueAt(row, 3).toString();               
                String altura = jTableJugadores.getValueAt(row, 4).toString();
                String posicion = jTableJugadores.getValueAt(row, 5).toString();
                String sueldo = jTableJugadores.getValueAt(row, 6).toString();
                
                InserAct abrir = new InserAct(id,nEquipo,nombre,apellidos,altura,posicion,sueldo);
                abrir.setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(Secundaria.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_actualizarActionPerformed

