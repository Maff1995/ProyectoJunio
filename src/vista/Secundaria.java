    public void rellenarTabla() throws SQLException{
        
        DefaultTableModel tablaJugador = (DefaultTableModel) jTableJugadores.getModel();
     
        ArrayList<Jugador> datos;
        datos=controlador.ControladorJugador.mostrarJugadores();
        System.out.println(datos);
        for (int i = 0; i < datos.size(); i++) {
            System.out.println(datos.get(i).getMiEquipo());
            int id= datos.get(i).getId();
            String nombre= datos.get(i).getNombre();
            String nEquipo=controlador.ControladorJugador.nombreEquipo(datos.get(i).getEquipo()) ;
            String apellido=datos.get(i).getApellidos();
            String posicion=datos.get(i).getPosicion();
            double altura=datos.get(i).getAltura();
            double sueldo= datos.get(i).getSueldo();
            Equipo miEquipo=datos.get(i).getMiEquipo();
            
            tablaJugador.addRow(new Object[]{id,nEquipo, nombre, apellido, altura, posicion, sueldo,miEquipo.getId()});

        }
        
           
    
    }
