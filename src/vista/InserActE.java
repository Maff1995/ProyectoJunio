    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:

        if ( jButtonAceptar.getText()=="Insertar"){
       
            try {
                ControladorEquipo.InsertarEquipo(jTextFieldNombre.getText().toString(),Double.parseDouble(jTextFieldPresupuesto.getText().toString()),ligas.getSelectedItem().toString());
                ligas.getModel().setSelectedItem("Elija una liga");
                    jTextFieldNombre.setText("");
                    jTextFieldPresupuesto.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(InserActE.class.getName()).log(Level.SEVERE, null, ex);
            }
        
       }else{
        
              try {
                  ControladorEquipo.actualizarEquipo(this.id,jTextFieldNombre.getText().toString(),ligas.getSelectedItem().toString(),Double.parseDouble(jTextFieldPresupuesto.getText().toString()));
              } catch (SQLException ex) {
                  Logger.getLogger(InserActE.class.getName()).log(Level.SEVERE, null, ex);
              }
        }
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed

