    private void jTextFieldPresupuestoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPresupuestoKeyTyped
        // TODO add your handling code here:
        int k = (int) evt.getKeyChar();
        if (k >= 97 && k <= 122 || k >= 65 && k <= 90) {
            evt.setKeyChar((char) KeyEvent.VK_CLEAR);
            JOptionPane.showMessageDialog(null, "Solo admite n√∫meros", "Error ", JOptionPane.ERROR_MESSAGE);
        }
        if (k == 241 || k == 209) {
            evt.setKeyChar((char) KeyEvent.VK_CLEAR);
            JOptionPane.showMessageDialog(null, "Solo admite n√∫meros", "Error ", JOptionPane.ERROR_MESSAGE);
        }
        if (k == 10) {
            jTextFieldPresupuesto.transferFocus();
        }
    }//GEN-LAST:event_jTextFieldPresupuestoKeyTyped

